char	*ft_strchr(const char *s, int c)
{
	const char	c2 = c;

	while (*s != '\0')
	{
		if (*s == c2)
			return ((char *)s);
		s++;
	}
	if (*s == c)
		return ((char *)s);
	return (0);
}
